    // Function to force two-column layout
    function forceTwoColumnLayout() {
        console.log('📐 FORCING TWO-COLUMN LAYOUT...');
        
        const educationTab = document.getElementById('education-tab');
        if (educationTab) {
            // Check if already has two-column structure
            if (!educationTab.querySelector('.education-two-column')) {
                // Find all sections
                const sections = educationTab.querySelectorAll('.education-section');
                
                if (sections.length >= 2) {
                    // Create two-column container
                    const twoColumnContainer = document.createElement('div');
                    twoColumnContainer.className = 'education-two-column';
                    twoColumnContainer.style.cssText = `
                        display: grid;
                        grid-template-columns: 1fr 1fr;
                        gap: 30px;
                        margin-top: 20px;
                    `;
                    
                    // Move sections into columns
                    sections.forEach((section, index) => {
                        if (index < 2) {
                            const column = document.createElement('div');
                            column.className = 'education-column';
                            column.appendChild(section.cloneNode(true));
                            twoColumnContainer.appendChild(column);
                        }
                    });
                    
                    // Replace original content
                    educationTab.innerHTML = '';
                    educationTab.appendChild(twoColumnContainer);
                }
            }
        }
    }

