    // Initialize enhancements
    function init() {
        console.log('🎯 Initializing Force Enhanced Layout with ScaleOps6 Branding...');
        
        // Run enhancements immediately
        forceEducationEnhancements();
        
        // Run again after a delay to catch any dynamic content
        setTimeout(forceEducationEnhancements, 2000);
        setTimeout(forceEducationEnhancements, 5000);
        
        // Watch for tab changes
        document.addEventListener('click', function(e) {
            if (e.target.matches('[data-tab="education"]')) {
                setTimeout(forceEducationEnhancements, 500);
            }
        });
        
        // Watch for navigation changes with debouncing to prevent loops
        let enhancementTimeout;
        const observer = new MutationObserver(() => {
            clearTimeout(enhancementTimeout);
            enhancementTimeout = setTimeout(() => {
                const educationTab = document.getElementById('education-tab');
                if (educationTab && educationTab.classList.contains('active')) {
                    // Only run if not already enhanced recently
                    if (!educationTab.dataset.enhanced || Date.now() - parseInt(educationTab.dataset.enhanced) > 5000) {
                        educationTab.dataset.enhanced = Date.now();
                        forceEducationEnhancements();
                    }
                }
            }, 1000);
        });
        
        // Only observe specific elements to reduce overhead
        const tabContainer = document.querySelector('.tab-navigation');
        if (tabContainer) {
            observer.observe(tabContainer, {
                attributes: true,
                attributeFilter: ['class'],
                subtree: true
            });
        }
    }

    // Start when DOM is ready
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', init);
    } else {
        init();
    }

