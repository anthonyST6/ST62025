/**
 * Get templates for a specific domain
 * @param {string} domain - The domain name (subcomponent name)
 * @returns {Array<string>} Array of template names
 */
function getTemplatesForDomain(domain) {
    const templates = DOMAIN_TEMPLATE_MAPPING[domain];
    
    if (!templates) {
        console.warn(`⚠️  No templates found for domain: ${domain}`);
        return [];
    }
    
    return templates;
}

/**
 * Validate that all domains in SSOT have templates
 * @param {Object} ssotRegistry - The complete SSOT registry
 * @returns {Array} Array of validation errors
 */
function validateTemplateRegistry(ssotRegistry) {
    const errors = [];
    const warnings = [];
    
    for (const [id, data] of Object.entries(ssotRegistry)) {
        const domain = data.name;
        const templates = DOMAIN_TEMPLATE_MAPPING[domain];
        
        if (!templates || templates.length === 0) {
            errors.push({
                subcomponentId: id,
                domain: domain,
                error: 'NO_TEMPLATES',
                message: `No templates defined for domain: ${domain}`
            });
        } else if (templates.length < 3) {
            warnings.push({
                subcomponentId: id,
                domain: domain,
                warning: 'INSUFFICIENT_TEMPLATES',
                message: `Only ${templates.length} templates defined (expected 3)`
            });
        }
    }
    
    return { errors, warnings };
}

/**
 * Get all domains that have templates
 * @returns {Array<string>} Array of domain names
 */
function getAllDomains() {
    return Object.keys(DOMAIN_TEMPLATE_MAPPING);
}

/**
 * Get template count statistics
 * @returns {Object} Statistics about templates
 */
function getTemplateStats() {
    const domains = Object.keys(DOMAIN_TEMPLATE_MAPPING);
    const totalTemplates = Object.values(DOMAIN_TEMPLATE_MAPPING)
        .reduce((sum, templates) => sum + templates.length, 0);
    
    return {
        totalDomains: domains.length,
        totalTemplates: totalTemplates,
        averageTemplatesPerDomain: Math.round(totalTemplates / domains.length * 10) / 10
    };
}

