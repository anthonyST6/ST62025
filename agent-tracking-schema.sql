-- ScaleOps6 Agent Tracking Database Schema
-- Complete audit trail and content management for all 96 agents

-- Main agent registry table
CREATE TABLE IF NOT EXISTS agents (
    id VARCHAR(10) PRIMARY KEY,  -- e.g., "1-1", "2-3"
    agent_key VARCHAR(5),         -- e.g., "1a", "2c"
    name VARCHAR(255) NOT NULL,
    description TEXT,
    block_number INT,
    subcomponent_number INT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_block (block_number),
    INDEX idx_subcomponent (block_number, subcomponent_number)
);

-- Agent-generated educational content
CREATE TABLE IF NOT EXISTS agent_educational_content (
    id INT AUTO_INCREMENT PRIMARY KEY,
    subcomponent_id VARCHAR(10),
    agent_id VARCHAR(10),
    title VARCHAR(255),
    executive_summary TEXT,
    what_content TEXT,
    why_content TEXT,
    how_content JSON,  -- Stores implementation steps as JSON
    examples JSON,     -- Stores examples as JSON
    metrics JSON,      -- Stores metrics as JSON
    best_practices JSON,
    generated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (agent_id) REFERENCES agents(id),
    INDEX idx_subcomponent (subcomponent_id),
    INDEX idx_generated (generated_at)
);

-- Workspace questions generated by agents
CREATE TABLE IF NOT EXISTS agent_workspace_questions (
    id INT AUTO_INCREMENT PRIMARY KEY,
    subcomponent_id VARCHAR(10),
    agent_id VARCHAR(10),
    question_id VARCHAR(20),
    dimension VARCHAR(255),
    question TEXT,
    question_type VARCHAR(50),  -- 'scale', 'textarea', 'select', etc.
    weight INT,
    required BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (agent_id) REFERENCES agents(id),
    INDEX idx_subcomponent (subcomponent_id)
);

-- User workspace answers
CREATE TABLE IF NOT EXISTS workspace_answers (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id VARCHAR(50),
    subcomponent_id VARCHAR(10),
    question_id VARCHAR(20),
    answer TEXT,
    score INT,
    submitted_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_user_subcomponent (user_id, subcomponent_id),
    INDEX idx_submitted (submitted_at)
);

-- Agent analysis results
CREATE TABLE IF NOT EXISTS agent_analysis_results (
    id INT AUTO_INCREMENT PRIMARY KEY,
    subcomponent_id VARCHAR(10),
    agent_id VARCHAR(10),
    user_id VARCHAR(50),
    total_score DECIMAL(5,2),
    performance_level VARCHAR(50),
    dimension_scores JSON,  -- Detailed scores per dimension
    strengths JSON,
    improvements JSON,
    recommendations JSON,
    next_steps JSON,
    analyzed_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (agent_id) REFERENCES agents(id),
    INDEX idx_user_subcomponent (user_id, subcomponent_id),
    INDEX idx_score (total_score),
    INDEX idx_analyzed (analyzed_at)
);

-- Resource templates generated by agents
CREATE TABLE IF NOT EXISTS agent_resource_templates (
    id INT AUTO_INCREMENT PRIMARY KEY,
    subcomponent_id VARCHAR(10),
    agent_id VARCHAR(10),
    template_name VARCHAR(255),
    template_type VARCHAR(50),  -- 'assessment', 'action_plan', 'guide', etc.
    description TEXT,
    sections JSON,  -- Template structure as JSON
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (agent_id) REFERENCES agents(id),
    INDEX idx_subcomponent (subcomponent_id),
    INDEX idx_type (template_type)
);

-- Template outputs (processed templates with user data)
CREATE TABLE IF NOT EXISTS template_outputs (
    id INT AUTO_INCREMENT PRIMARY KEY,
    subcomponent_id VARCHAR(10),
    agent_id VARCHAR(10),
    user_id VARCHAR(50),
    template_id INT,
    output_content JSON,  -- Generated content
    generated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (agent_id) REFERENCES agents(id),
    FOREIGN KEY (template_id) REFERENCES agent_resource_templates(id),
    INDEX idx_user_subcomponent (user_id, subcomponent_id),
    INDEX idx_generated (generated_at)
);

-- Score history tracking
CREATE TABLE IF NOT EXISTS score_history (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id VARCHAR(50),
    subcomponent_id VARCHAR(10),
    agent_id VARCHAR(10),
    block_number INT,
    score DECIMAL(5,2),
    previous_score DECIMAL(5,2),
    improvement DECIMAL(5,2),
    dimension_scores JSON,
    recorded_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (agent_id) REFERENCES agents(id),
    INDEX idx_user_subcomponent (user_id, subcomponent_id),
    INDEX idx_block (block_number),
    INDEX idx_recorded (recorded_at)
);

-- Comprehensive audit trail
CREATE TABLE IF NOT EXISTS agent_audit_trail (
    id INT AUTO_INCREMENT PRIMARY KEY,
    agent_id VARCHAR(10),
    agent_name VARCHAR(255),
    subcomponent_id VARCHAR(10),
    block_number INT,
    action VARCHAR(100),  -- 'content_generated', 'workspace_analyzed', 'template_processed', etc.
    user_id VARCHAR(50),
    data JSON,  -- Additional context data
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (agent_id) REFERENCES agents(id),
    INDEX idx_agent (agent_id),
    INDEX idx_subcomponent (subcomponent_id),
    INDEX idx_action (action),
    INDEX idx_timestamp (timestamp),
    INDEX idx_user (user_id)
);

-- Agent performance metrics
CREATE TABLE IF NOT EXISTS agent_performance_metrics (
    id INT AUTO_INCREMENT PRIMARY KEY,
    agent_id VARCHAR(10),
    total_analyses INT DEFAULT 0,
    average_score DECIMAL(5,2),
    success_rate DECIMAL(5,2),  -- Percentage of analyses with score > 75
    total_templates_generated INT DEFAULT 0,
    total_outputs_created INT DEFAULT 0,
    last_active TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (agent_id) REFERENCES agents(id),
    INDEX idx_agent (agent_id),
    INDEX idx_performance (average_score, success_rate)
);

-- Block-level aggregated data
CREATE TABLE IF NOT EXISTS block_performance (
    id INT AUTO_INCREMENT PRIMARY KEY,
    block_number INT,
    block_name VARCHAR(255),
    total_subcomponents INT,
    average_score DECIMAL(5,2),
    completion_rate DECIMAL(5,2),
    last_updated TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_block (block_number),
    INDEX idx_score (average_score)
);

-- User progress tracking
CREATE TABLE IF NOT EXISTS user_progress (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id VARCHAR(50),
    block_number INT,
    subcomponents_completed INT,
    total_subcomponents INT,
    average_score DECIMAL(5,2),
    last_activity TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_user (user_id),
    INDEX idx_block (block_number),
    INDEX idx_progress (subcomponents_completed, total_subcomponents)
);

-- Agent recommendations tracking
CREATE TABLE IF NOT EXISTS agent_recommendations (
    id INT AUTO_INCREMENT PRIMARY KEY,
    agent_id VARCHAR(10),
    user_id VARCHAR(50),
    subcomponent_id VARCHAR(10),
    recommendation_type VARCHAR(50),
    recommendation TEXT,
    priority VARCHAR(20),  -- 'Critical', 'High', 'Medium', 'Low'
    status VARCHAR(20) DEFAULT 'Open',  -- 'Open', 'In Progress', 'Completed'
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    completed_at TIMESTAMP NULL,
    FOREIGN KEY (agent_id) REFERENCES agents(id),
    INDEX idx_user (user_id),
    INDEX idx_status (status),
    INDEX idx_priority (priority)
);

