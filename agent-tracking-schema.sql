-- Create views for reporting
CREATE VIEW agent_activity_summary AS
SELECT 
    a.id,
    a.name,
    a.block_number,
    COUNT(DISTINCT aat.id) as total_actions,
    COUNT(DISTINCT aar.id) as total_analyses,
    AVG(aar.total_score) as avg_score,
    MAX(aat.timestamp) as last_activity
FROM agents a
LEFT JOIN agent_audit_trail aat ON a.id = aat.agent_id
LEFT JOIN agent_analysis_results aar ON a.id = aar.agent_id
GROUP BY a.id, a.name, a.block_number;

CREATE VIEW block_completion_status AS
SELECT 
    bp.block_number,
    bp.block_name,
    bp.total_subcomponents,
    COUNT(DISTINCT sh.subcomponent_id) as completed_subcomponents,
    AVG(sh.score) as average_score,
    MAX(sh.recorded_at) as last_activity
FROM block_performance bp
LEFT JOIN score_history sh ON bp.block_number = sh.block_number
GROUP BY bp.block_number, bp.block_name, bp.total_subcomponents;